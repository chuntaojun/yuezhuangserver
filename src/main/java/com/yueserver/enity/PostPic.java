package com.yueserver.enity;

public class PostPic {

    private int postpicno;
    private String postpicurl;
    private int postpicloc;

    public PostPic(){}

    public int getPostpicno() {
        return postpicno;
    }

    public void setPostpicno(int postpicno) {
        this.postpicno = postpicno;
    }

    public String getPostpicurl() {
        return postpicurl;
    }

    public void setPostpicurl(String postpicurl) {
        this.postpicurl = postpicurl;
    }

    public int getPostpicloc() {
        return postpicloc;
    }

    public void setPostpicloc(int postpicloc) {
        this.postpicloc = postpicloc;
    }
}
