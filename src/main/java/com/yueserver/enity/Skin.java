public class Skin {

    private int skinno;
    private String skinname;
    private String skinintro;

    public Skin() {
    }

    public int getSkinno() {
        return skinno;
    }

    public void setSkinno(int skinno) {
        this.skinno = skinno;
    }

    public String getSkinname() {
        return skinname;
    }

    public void setSkinname(String skinname) {
        this.skinname = skinname;
    }

    public String getSkinintro() {
        return skinintro;
    }

    public void setSkinintro(String skinintro) {
        this.skinintro = skinintro;
    }
}
